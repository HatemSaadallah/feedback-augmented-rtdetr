"""Parse the four eval logs produced by eval_all_p2.sbatch and emit a clean
markdown + LaTeX table comparing baseline vs feedback at 640 and 800 input.

Reads:
    /home/3415496/rt-detr/eval_p2_baseline_640.log
    /home/3415496/rt-detr/eval_p2_baseline_800.log
    /home/3415496/rt-detr/eval_p2_feedback_p2_640.log
    /home/3415496/rt-detr/eval_p2_feedback_p2_800.log
(or local paths if --logdir given)

Writes both a Markdown table to stdout and a LaTeX table to --tex.
"""

import argparse
import glob
import os
import re
import sys
from typing import Optional


METRICS = [
    ("AP",   r"area=   all \| maxDets=100"),
    ("AP_50", r"IoU=0\.50      \| area=   all \| maxDets=100"),
    ("AP_75", r"IoU=0\.75      \| area=   all \| maxDets=100"),
    ("AP_S", r"area= small"),
    ("AP_M", r"area=medium"),
    ("AP_L", r"area= large"),
]


def parse_log(path: str) -> dict:
    out = {}
    if not os.path.exists(path):
        return out
    text = open(path).read()
    # AP block (Average Precision lines)
    for name, pat in METRICS:
        m = re.search(rf" Average Precision  \(AP\) @\[ {pat}.* = ([\d.]+)", text)
        if m:
            out[name] = float(m.group(1))
    return out


def to_pct(x: Optional[float]) -> str:
    return f"{x*100:.1f}" if x is not None else "—"


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--logdir", default="/home/3415496/rt-detr",
                    help="Directory containing eval_p2_*.log files")
    ap.add_argument("--tex", default=None, help="Optional path to write LaTeX table")
    args = ap.parse_args()

    cells = ["baseline_640", "baseline_800", "feedback_p2_640", "feedback_p2_800"]
    rows = {c: parse_log(os.path.join(args.logdir, f"eval_p2_{c}.log")) for c in cells}

    metric_names = [m for m, _ in METRICS]

    # Baseline reference for delta computation (use baseline_640)
    base = rows.get("baseline_640", {})

    # ---- Markdown ----
    md = []
    md.append("| Setting | " + " | ".join(metric_names) + " | ΔAP_S |")
    md.append("|---|" + "---|" * (len(metric_names) + 1))
    pretty = {
        "baseline_640":     "RT-DETR R50 (baseline) @ 640",
        "baseline_800":     "RT-DETR R50 (baseline) @ 800",
        "feedback_p2_640":  "**Feedback-augmented RT-DETR + P2 + 500q @ 640 (ours)**",
        "feedback_p2_800":  "**Feedback-augmented RT-DETR + P2 + 500q @ 800 (ours)**",
    }
    for c in cells:
        r = rows.get(c, {})
        delta = ""
        if "AP_S" in r and "AP_S" in base:
            d = (r["AP_S"] - base["AP_S"]) * 100
            delta = f"{d:+.1f}"
        md.append("| " + pretty[c] + " | " +
                  " | ".join(to_pct(r.get(m)) for m in metric_names) +
                  f" | {delta} |")
    print("\n".join(md))
    print()

    # ---- LaTeX ----
    if args.tex:
        with open(args.tex, "w") as f:
            f.write("% Auto-generated by tools/build_results_table.py\n")
            f.write("\\begin{table}[t]\n\\centering\n\\small\n")
            f.write("\\begin{tabular}{l" + "c" * (len(metric_names) + 1) + "}\n\\toprule\n")
            f.write("Method & " + " & ".join(metric_names) + " & $\\Delta$AP$_S$ \\\\\n\\midrule\n")
            for c in cells:
                r = rows.get(c, {})
                delta = ""
                if "AP_S" in r and "AP_S" in base:
                    d = (r["AP_S"] - base["AP_S"]) * 100
                    delta = f"{d:+.1f}"
                latex_name = pretty[c].replace("**", "").replace("@", "@").replace("_", "\\_")
                f.write(latex_name + " & " +
                        " & ".join(to_pct(r.get(m)) for m in metric_names) +
                        f" & {delta} \\\\\n")
            f.write("\\bottomrule\n\\end{tabular}\n")
            f.write("\\caption{COCO val2017 results. AP$_S$ delta is over the published RT-DETR R50 baseline at 640.}\n")
            f.write("\\label{tab:main_results}\n\\end{table}\n")
        print(f"\nLaTeX table written to {args.tex}")


if __name__ == "__main__":
    main()
